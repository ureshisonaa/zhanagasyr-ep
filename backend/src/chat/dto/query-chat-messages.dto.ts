import { Type } from 'class-transformer';
import { IsInt, IsOptional, IsUUID, Max, Min } from 'class-validator';

/**
 * Не полноценная пагинация (page/limit), а простое ограничение "последние
 * N сообщений" — паттерн, естественный для чата (пользователь листает
 * вверх за историей, а не переключает страницы). ТЗ (Часть 2, п.23)
 * прямо предупреждает про масштаб "миллионы сообщений" — отдавать
 * историю целиком неприемлемо.
 *
 * `before` (Этап 12.2, ревизия пагинации) — курсор для подгрузки более
 * старых сообщений сверх первого запроса: без него при 1000+ сообщениях
 * в диалоге не было способа увидеть что-либо старше последних 200 (max
 * limit) — только начальная загрузка, без "прокрутки вверх за историей".
 */
export class QueryChatMessagesDto {
  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  @Max(200)
  public limit: number = 50;

  @IsOptional()
  @IsUUID()
  public before?: string;
}
